fun main(args: Array<String>) {
    println("""!@#$%^&*(\'"<>?:;""")
} // 나의 답변

// """ """ 이것을 사용하면 이스케이프 시퀸스가 없더라도 문자열을 그대로 입력을 할 수 있기에 """ """을 사용하여 답변을 하였다.

fun main(args: Array<String>) {
    println("!@#$%^&*(\\'\"<>?:;")
} // 다른 사람의 답변

